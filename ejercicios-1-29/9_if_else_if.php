<?php
// ELSE IF (Para multiples condiciones)
$diaNumero = 3;

if ($diaNumero == 1) {
    echo "lunes";
} else if ($diaNumero == 2) {
    echo "martes";
} else if ($diaNumero == 3) {
    echo "miercoles";
} else if ($diaNumero == 4) {
    echo "jueves";
} else if ($diaNumero == 5) {
    echo "viernes";
} else if ($diaNumero == 6) {
    echo "sabado";
} else if ($diaNumero == 7) {
    echo "domingo";
} else {
    echo "dia no valido";
}

echo "<hr>";
?>